#include<stdio.h>

int main(){
    int age = 20;
    if (age>18)
    {
        printf("You are above 18");
    }else{
        printf("You are under 18");
    }
    /*
    Relational Operators are used to evalaute conditions (true or false) inside the if statements
    '>'  Greater than
    '<'  Less than
    '==' Equals  (Dont confuse, '=' is used for assignment)
    '>=' Greater than or Equal to
    '<=' Less than or Equal to
    '!=' Not equal to
    */
    
    return 0;
}